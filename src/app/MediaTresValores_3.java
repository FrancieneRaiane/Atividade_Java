package app;

public class MediaTresValores_3 {
    public static void main(String[] args) {
        // Valores fixos
        double valor1 = 8.5;
        double valor2 = 7.0;
        double valor3 = 9.5;

        // Calcula a média
        double media = (valor1 + valor2 + valor3) / 3;

        // Exibe o resultado
        System.out.println("A média dos três valores é: " + media);
    }
}
